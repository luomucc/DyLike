    // LRU 字符串长度缓存，限制最大 5000 条
    private const val MAX_CACHE_SIZE = 5000
    private val stringLengthCache = Collections.synchronizedMap(
        object : LinkedHashMap<String, Int>(MAX_CACHE_SIZE + 1, 0.75f, true) {
            override fun removeEldestEntry(eldest: MutableMap.MutableEntry<String, Int>?): Boolean {
                return size > MAX_CACHE_SIZE
            }
        }
    )

    fun formatTime(seconds: Float): String {
        val hours = (seconds / 3600).toInt()
        val minutes = ((seconds % 3600) / 60).toInt()
        val secs = (seconds % 60).toInt()
        val centiSeconds = ((seconds % 1) * 100).toInt()

        return String.format(Locale.CHINA, "%d:%02d:%02d.%02d", hours, minutes, secs, centiSeconds)
    }

    fun getStringLength(text: String, fontSize: Int): Int {
        // 先查缓存（不包含 fontSize 的计算结果）
        val cachedWidth = stringLengthCache[text]
        val width = if (cachedWidth != null) {
            cachedWidth
        } else {
            var calculatedWidth = 0
            for (char in text) {
                calculatedWidth += when {
                    char.code > 0x2E80 -> 2  // 中文字符
                    char in "[](){}「」【】《》" -> 2
                    else -> 1
                }
            }
            stringLengthCache[text] = calculatedWidth
            calculatedWidth
        }
        return width * (fontSize / 2)
    }

    fun getColorByPrice(price: Int): Triple<String, String, String> {
        return when {
            price >= 2000 -> Triple("\\c&HD8D8FF", "\\c&H321AAB", "\\c&H1B0E5E")
            price >= 1000 -> Triple("\\c&HE4E7FF", "\\c&H4D4DE5", "\\c&H333398")
            price >= 500 -> Triple("\\c&HD2EAFF", "\\c&H4394E0", "\\c&H2C6193")
            price >= 100 -> Triple("\\c&HC5F1FF", "\\c&H2BB5E2", "\\c&H1C7795")
            price >= 50 -> Triple("\\c&HFDFFDB", "\\c&H9E7D42", "\\c&H514022")
            else -> Triple("\\c&HFFF5ED", "\\c&HB2602A", "\\c&H653617")
        }
    }

    fun removeEmojis(text: String, replacement: String = ""): String {
        return EMOJI_REGEX.replace(text, replacement)
    }

